
operators in numbers
print(10+10)
print(10-5)
print(10*5)
print(10/2) #gives float value
print(10//2) #gives integer value by removing decimal values
print(10%3) #gives remainder value
print(10**3) #gives power value Exponent
print((10+5)*3) #gives 45 because of BODMAS rule

# Methods in numbers
import math
print(round(3.7)) #gives rounded value
print(abs(-10)) #gives absolute value
print(pow(2,3)) #gives power value
math.ceil(3.4) #gives next integer value
math.floor(3.7) #gives previous integer value
math.sqrt(16) #gives square root value
math.factorial(5) #gives factorial value
math.gcd(12,15) #gives greatest common divisor value
math.sin(90) #gives sine value
math.cos(0) #gives cosine value
math.tan(45) #gives tangent value
math.log(100,10) #gives logarithm value with base 10
math.exp(2) #gives exponential value e^2
math.radians(90) #converts degrees to radians
math.degrees(3.14) #converts radians to degrees